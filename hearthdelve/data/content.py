# --- depth-scaled gear loot --------------------------------------------------
# Only good affixes drop as loot (a reward); the chance climbs with depth.
_LOOT_PREFIXES = ("fine", "keen", "heavy", "masterwork", "reinforced")
_LOOT_SUFFIXES = ("slaying", "accuracy", "warding", "protection")
_METAL_BY_TIER = {MATERIALS[m].tier: m for m in FORGE_METALS}   # tiers 2..8, contiguous


def _loot_material(depth: int, rng, allow_leather: bool) -> str:
    """Depth sets the expected metal tier, with a pinch of random salt: usually a
    small drift up or down, and now and then a real surprise (a lucky mithril
    find deep-shallow, or a humble copper deep down) — organic, not a fixed
    table."""
    if allow_leather and depth <= 2 and rng.random() < 0.35:
        return "leather"
    target = 2.0 + depth * 0.8                      # depth 1 -> ~bronze, 6 -> ~adamantium
    jitter = rng.uniform(-1.3, 1.3)                 # the usual small drift
    if rng.random() < 0.12:                         # the pinch of salt — a surprise
        jitter += rng.choice((-3, -2, 2, 3))
    tier = max(2, min(8, round(target + jitter)))
    return _METAL_BY_TIER[tier]


def _loot_soft(depth: int, rng) -> str:
    """A cloth/hide material for a hat or cloak, scaled to depth (light salt)."""
    idx = round(min(len(SOFT_ARMOR_MATERIALS) - 1, depth * 0.55) + rng.uniform(-1.2, 1.2))
    return SOFT_ARMOR_MATERIALS[max(0, min(len(SOFT_ARMOR_MATERIALS) - 1, idx))]


def _loot_wood(depth: int, rng) -> str:
    """A bow wood scaled to depth (birch..composite, tiers 1-4), same pinch of
    salt as the metals."""
    target = 1.0 + depth * 0.45
    jitter = rng.uniform(-0.9, 0.9)
    if rng.random() < 0.12:
        jitter += rng.choice((-2, 2))
    tier = max(1, min(len(BOW_WOODS), round(target + jitter)))
    return BOW_WOODS[tier - 1]


def _roll_affix(pool, kind: str, depth: int, rng, base: float) -> str:
    if rng.random() >= min(0.5, base + 0.045 * depth):
        return ""
    choices = [k for k in pool if kind in (PREFIXES | SUFFIXES)[k].kinds]
    return rng.choice(choices) if choices else ""


# Smithing: a seasoned smith works a bonus affix into a forged piece. The chance
# and the best affix both climb with skill; (min-level, key) pairs, best last.
_SMITH_PREFIX_LADDER = [(2, "fine"), (5, "keen"), (8, "masterwork")]
_SMITH_SUFFIX_LADDER = [(4, "accuracy"), (7, "slaying")]


def roll_craft_affix(level: int, rng, kind: str) -> tuple[str, str]:
    """The (prefix, suffix) a smith of the given Smithing level might forge into a
    piece — usually ('', '') at low skill, better and likelier as it climbs.
    ``kind`` is 'weapon' or 'armor' (affix pools are filtered to what fits)."""
    prefix = suffix = ""
    if rng.random() < min(0.55, 0.04 * level):
        opts = [k for lv, k in _SMITH_PREFIX_LADDER if level >= lv and kind in PREFIXES[k].kinds]
        if opts:
            prefix = opts[-1]
    if rng.random() < min(0.40, 0.03 * level):
        opts = [k for lv, k in _SMITH_SUFFIX_LADDER if level >= lv and kind in SUFFIXES[k].kinds]
        if opts:
            suffix = opts[-1]
    return prefix, suffix


def random_gear(depth: int, rng, kind: str | None = None) -> Item:
    """A weapon, bow, or armour piece scaled to dungeon depth, with a
    depth-rising chance of a good prefix and/or suffix."""
    kind = kind or rng.choices(("weapon", "armor", "ranged"), weights=(4, 4, 2))[0]
    affix_kind = kind                       # bows filter to ranged-appropriate affixes only
    if kind == "ranged":
        base = rng.choice(list(RANGED_BASES))
        mat = _loot_wood(depth, rng)
    elif kind == "weapon":
        base = rng.choice(list(WEAPON_BASES))
        mat = _loot_material(depth, rng, allow_leather=False)
    else:
        base = rng.choice(list(ARMOR_BASES))
        mat = _loot_soft(depth, rng) if base in _SOFT_BASES \
            else _loot_material(depth, rng, allow_leather=True)
    pfx = _roll_affix(_LOOT_PREFIXES, affix_kind, depth, rng, 0.08)
    sfx = _roll_affix(_LOOT_SUFFIXES, affix_kind, depth, rng, 0.06)
    if kind == "ranged":
        return make_ranged(base, mat, pfx, sfx)
    return make_gear(base, mat, pfx, sfx)


@dataclass(frozen=True)
class RangedStat:
    category: str          # mastery bucket (all ranged share "archery")
    to_hit: int            # flat bonus to the to-hit roll
    dmg: tuple             # (min, max) damage before bonuses
    rng: int               # how far it can reach
    ammo: Item             # the ammunition it spends


RANGED: dict[Item, RangedStat] = {}

# Bow bases: their stats are the birch baseline; the wood (and affixes) add on.
#   base -> (to_hit, (lo,hi), range, ammo, glyph, base_value)
RANGED_BASES: dict[str, tuple] = {
    "Short Bow": (1, (2, 5), 7, items.ARROW, ")", 90),
    "Long Bow":  (2, (4, 8), 9, items.ARROW, "}", 190),
}
_ALL_BASES |= set(RANGED_BASES)
_CANON_RANGED: dict[tuple, Item] = {
    ("Short Bow", "birch", "", ""): items.SHORT_BOW,
    ("Long Bow", "yew", "", ""):    items.LONG_BOW,
}
_RANGED_CACHE: dict[tuple, Item] = {}


def make_ranged(base: str, wood: str, prefix: str = "", suffix: str = "") -> Item:
    """Compose (and register + stat) a bow from base + wood + affixes."""
    key = (base, wood, prefix, suffix)
    if key in _RANGED_CACHE:
        return _RANGED_CACHE[key]
    mat = MATERIALS[wood]
    pfx = PREFIXES.get(prefix)
    sfx = SUFFIXES.get(suffix)
    bth, (lo, hi), brng, ammo, glyph, bval = RANGED_BASES[base]
    to_hit = bth + mat.w_to_hit + (pfx.to_hit if pfx else 0) + (sfx.to_hit if sfx else 0)
    db = mat.w_dmg + (pfx.dmg if pfx else 0) + (sfx.dmg if sfx else 0)
    rng_ = brng + mat.r_rng
    name = _compose_name(base, wood, prefix, suffix)
    it = _CANON_RANGED.get(key)
    if it is None:
        it = items.register(items.Item(name, glyph, "ranged", f"A {wood} {base.lower()}.",
                                       stackable=False, value=_affix_value(bval, mat, pfx, sfx),
                                       material=wood, prefix=prefix, suffix=suffix))
    RANGED[it] = RangedStat("archery", to_hit, (max(1, lo + db), max(1, hi + db)), rng_, ammo)
    _RANGED_CACHE[key] = it
    return it


