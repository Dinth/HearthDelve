def wildlife_drops(name: str, rng) -> list:
    """Roll a slain wild creature's yield: a list of (item, qty)."""
    out = []
    for item, chance, (lo, hi) in WILDLIFE_DROPS.get(name, ()):
        if rng.random() < chance:
            out.append((item, rng.randint(lo, hi)))
    return out


