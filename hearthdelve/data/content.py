# --- Per-source artisan goods ------------------------------------------------
# A jam/wine/pickle inherits the value of the fruit or vegetable it's made from,
# plus a crafting premium (and, at sell time, a quality-star bonus). Rather than
# hand-author "Strawberry Jam", "Raspberry Jam", ... we generate one variant per
# source; each carries its `source` and a shared `family` so gift tastes match
# "any jam". Grape wine keeps its hand-tuned premium item.
JAM_MULT, WINE_MULT, PICKLE_MULT = 1.6, 1.8, 1.5
GRAPE_WINE_MULT = 2.5           # grape is *the* wine fruit — its vintage tops every other

_FRUIT_ITEMS = [c.produce for c in CROPS if c.category == "fruit"] + \
    [items.RASPBERRY, items.GOOSEBERRY, items.CURRANT,
     items.CHERRY, items.PEACH, items.APPLE, items.PEAR, items.ORANGE]
_VEG_ITEMS = [c.produce for c in CROPS if c.category == "vegetable"]


def _variant(name: str, glyph: str, source: Item, mult: float, family: str) -> Item:
    return items.register(items.Item(
        name, glyph, "artisan", f"{family.capitalize()} made from {source.name.lower()}.",
        value=round(source.value * mult), family=family, source=source))


FRUIT_JAM = {f: _variant(f"{f.name} Jam", "■", f, JAM_MULT, "jam") for f in _FRUIT_ITEMS}
FRUIT_WINE = {}
# Apples and pears ferment into their own named drinks, not generic "wine".
_NAMED_FERMENT = {items.APPLE: items.CIDER, items.PEAR: items.PERRY}
for _f in _FRUIT_ITEMS:
    if _f is items.GRAPE:                       # keep the hand-tuned item, price it as the premium
        object.__setattr__(items.GRAPE_WINE, "value", round(items.GRAPE.value * GRAPE_WINE_MULT))
        FRUIT_WINE[_f] = items.GRAPE_WINE
    elif _f in _NAMED_FERMENT:                   # apple -> cider, pear -> perry
        drink = _NAMED_FERMENT[_f]
        object.__setattr__(drink, "value", round(_f.value * WINE_MULT))
        object.__setattr__(drink, "source", _f)
        FRUIT_WINE[_f] = drink
    else:
        FRUIT_WINE[_f] = _variant(f"{_f.name} Wine", "ø", _f, WINE_MULT, "wine")
VEG_PICKLE = {v: _variant(f"Pickled {v.name}", "■", v, PICKLE_MULT, "pickles") for v in _VEG_ITEMS}

# The cellar (a farmhouse upgrade) matures every drink into an aged vintage —
# same family, so a wine-lover's gift tastes cover the aged bottle too.
AGE_MULT = 1.5
AGED_DRINK: dict[Item, Item] = {}
for _d in sorted(set(FRUIT_WINE.values()), key=lambda i: i.name):
    AGED_DRINK[_d] = items.register(items.Item(
        f"Aged {_d.name}", _d.glyph, "artisan",
        f"{_d.name} matured in the cellar; deep and mellow.",
        value=round(_d.value * AGE_MULT), family=_d.family, source=_d.source))


