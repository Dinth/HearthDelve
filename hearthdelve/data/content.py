# monster name -> list of (item, drop chance)
MONSTER_DROPS: dict[str, list] = {
    "Cave Slime":   [(items.SLIME_GEL, 0.7)],
    "Bat":          [(items.BAT_WING, 0.6)],
    "Boar":         [(items.BOAR_HIDE, 0.5)],
    "Cave Spider":  [(items.SPIDER_SILK, 0.6)],
    "Deep Lurker":  [(items.LURKER_SCALE, 0.6)],
    "Wraith":       [(items.WRAITH_ESSENCE, 0.5)],
    "Cave Troll":   [(items.BOAR_HIDE, 1.0), (items.COAL, 1.0)],
    "Gloom Warden": [(items.WRAITH_ESSENCE, 1.0), (items.LURKER_SCALE, 0.8)],
}


def monster_drops(name: str, rng, level: int = 1) -> list:
    """Roll a slain creature's reagent drops. A higher-``level`` specimen (see
    :func:`make_mob`) drops more reliably, may yield a second reagent, and — for
    a real bruiser — leaves a depth-appropriate ore or gem as a trophy, so
    fighting the tougher monsters is worth the risk."""
    out = []
    bonus = 0.04 * max(0, level - 1)
    for item, chance in MONSTER_DROPS.get(name, ()):
        if rng.random() < min(0.95, chance + bonus):
            out.append(item)
            if level >= 4 and rng.random() < 0.07 * (level - 3):
                out.append(item)                       # a hefty specimen yields extra
    if level >= 3 and rng.random() < 0.06 * (level - 2):
        out.append(gem_for_depth(level, rng) if rng.random() < 0.3
                   else ore_for_depth(level, rng))     # a trophy from a tough kill
    return out


def chest_loot(depth: int, rng) -> tuple[int, list]:
    """Contents of a dungeon chest: (gold, [items]). Richer the deeper you go."""
    gold = rng.randint(30, 70) + depth * 15
    items_out = []
    # a metal bar or ore for the depth
    items_out.append(ore_for_depth(depth, rng))
    roll = rng.random()
    if roll < 0.35:                       # a gem
        items_out.append(gem_for_depth(depth, rng))
    elif roll < 0.6:                      # a couple of bombs
        items_out.append(items.BOMB)
    if rng.random() < 0.4:                # a cave snack
        items_out.append(items.CAVE_MUSHROOM)
    if rng.random() < 0.22 + 0.02 * depth:   # a piece of gear, scaled to depth
        items_out.append(random_gear(depth, rng))
    return gold, items_out


# --- fuels & smelting heat ---------------------------------------------------
# Every fuel has a heat rating. A metal needs a minimum heat to smelt at all, and
# hotter fuel smelts faster (excess heat above the minimum shortens the burn).
FUELS = {
    items.WOOD:     1,   # scraps by; only the softest metals
    items.CHARCOAL: 2,   # kiln-charred wood; a reliable mid fuel
    items.COAL:     3,   # mined; hot enough for the precious & hard metals
    items.COKE:     4,   # kiln-baked coal; the hottest — smelts anything, fast
}
# Ordered hottest-first, so the loader can offer the best fuel a player carries.
FUELS_BY_HEAT = sorted(FUELS, key=lambda f: -FUELS[f])


def smelt_time(min_heat: int, fuel_heat: int) -> int:
    """Minutes to smelt: harder metals take longer, hotter fuel is faster.
    (Only called when fuel_heat >= min_heat, so speed >= 1.)"""
    base = 40 + 25 * min_heat
    speed = fuel_heat / min_heat
    return max(20, round(base / speed))


